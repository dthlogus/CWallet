    private void montarComboCategoria() {
        try {
            ArrayList<Categoria> categorias = categoriaControler.listagem();
            String[] categoriasArray = new String[categorias.size()];
            for (int index = 0; index < categorias.size(); index++) {
                categoriasArray[index] = categorias.get(index).getNome();
            }
            DefaultComboBoxModel model = new DefaultComboBoxModel(categoriasArray);
            jComboBoxCategoria.setModel(model);
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        }
    }

    private void limparTela() {
        jTextFieldId.setText("");
        jTextFieldDescricao.setText("");
        jFormattedTextFieldDataCompra.setText(" / ");
        jFormattedTextFieldDataCompra.setText("");
        jFormattedTextFieldDataCompra.setText(" / ");
        jRadioButtonReceita.setSelected(true);
        montarComboCategoria();
    }

    private void mostrarListagem() throws Exception {
        try {
            ArrayList<ReceitaDespesa> receitasDespesas = rdControle.listagem();
            DefaultTableModel model = (DefaultTableModel) jTableRD.getModel();
            model.setNumRows(0);

            for (ReceitaDespesa receitaDespesa : receitasDespesas) {
                String[] receitasSeparadas = new String[9];
                receitasSeparadas[0] = receitaDespesa.getId() + "";
                receitasSeparadas[1] = receitaDespesa.getNome();
                receitasSeparadas[2] = receitaDespesa.getTipoReceita();
                receitasSeparadas[3] = receitaDespesa.getDataDaCompra();
                receitasSeparadas[4] = receitaDespesa.getCategoria();
                receitasSeparadas[5] = "R$ " + receitaDespesa.getValor().toString();
                receitasSeparadas[6] = receitaDespesa.getParcelaAtual() + " / " + receitaDespesa.getParcela();
                receitasSeparadas[7] = receitaDespesa.getTipoEntrada();
                receitasSeparadas[8] = receitaDespesa.isRepetitivo() == true ? "Repetitivo" : "Não repetitivo";
                model.addRow(receitasSeparadas);
            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }
    }

