    private void ConsultaPorIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConsultaPorIdActionPerformed
        try {
            String str = jTextFieldId.getText();
            DefaultTableModel model = (DefaultTableModel) jTableRD.getModel();
            model.setNumRows(0);
            if (str.length() == 0) {
                limparTela();
                mostrarListagem();
                throw new Exception("O Identificador esta VAZIO!");
            }
            int id = Integer.parseInt(str);
            ReceitaDespesa rd = rdControle.consultarPorId(id);
            if (rd == null) {
                limparTela();
                mostrarListagem();
                throw new Exception("Categoria não existe no sistema");
            }
            String[] receitasSeparadas = new String[9];
            receitasSeparadas[0] = rd.getId() + "";
            receitasSeparadas[1] = rd.getNome();
            receitasSeparadas[2] = rd.getTipoReceita();
            receitasSeparadas[3] = rd.getDataDaCompra();
            receitasSeparadas[4] = rd.getCategoria();
            receitasSeparadas[5] = "R$ " + rd.getValor().toString();
            receitasSeparadas[6] = rd.getParcelaAtual() + " / " + rd.getParcela();
            receitasSeparadas[7] = rd.getTipoEntrada();
            receitasSeparadas[8] = rd.isRepetitivo() == true ? "Repetitivo" : "Não repetitivo";
            model.addRow(receitasSeparadas);
            limparTela();
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }
    }//GEN-LAST:event_ConsultaPorIdActionPerformed

