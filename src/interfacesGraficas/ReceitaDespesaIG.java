    private void jButtonIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncluirActionPerformed
        try {
            ReceitaDespesa rd = new ReceitaDespesa();
            if (jTextFieldId.getText().isEmpty() || jTextFieldId.getText() == " ") {
                throw new Exception("Por favor preenchar o campo de ID");
            }
            String[] parcelasSeparadas = jFormattedTextFieldParcelas.getText().split("/");
            rd.setId(Integer.parseInt(jTextFieldId.getText()));
            rd.setNome(jTextFieldDescricao.getText());
            rd.setTipoReceita(jComboBoxFormaPagamento.getSelectedItem().toString());
            rd.setDataDaCompra(jFormattedTextFieldDataCompra.getText());
            rd.setCategoria(jComboBoxCategoria.getSelectedItem().toString());
            rd.setValor(new BigDecimal(jTextFieldValor.getText()));
            rd.setParcelaAtual(Integer.parseInt(parcelasSeparadas[0]));
            rd.setParcela(Integer.parseInt(parcelasSeparadas[1]));
            if (jRadioButtonReceita.isSelected()) {
                rd.setTipoEntrada("Receita");
            } else {
                rd.setTipoEntrada("Custo");
            }
            rd.setRepetitivo(jRadioButtonRepetitivo.isSelected());
            rdControle.incluir(rd);
            limparTela();
            mostrarListagem();
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }
    }//GEN-LAST:event_jButtonIncluirActionPerformed

